package exercicios;

public class Triangulo {
	/*
	 * A classe le e soma três valoes, e indica se os valores formam
	 * um triangulo: equilátero, isósceles ou escaleno.
	 * 
	 * @author Andrei Luís Amancio
	 */
	
	public static void main(String[] args) {  
		int m1;
		int m2;
		int m3;
	    
		int triangulo = m1 + m2 + m3;
		
	   
		if (triangulo = (m1 = m2 = m3)){
			System.out.println("os tre valores formam um triagulo equilátero ");
		}else{
			if (triangulo = (m1 > m2 = m3)){
				System.out.println("os tre valores formam um triagulo isósceles ");
			}else{
				if (triangulo = (m1 > m2 > m3)){
					System.out.println("os tre valores formam um triagulo escaleno ");
				}
			}
		}
	}
}
